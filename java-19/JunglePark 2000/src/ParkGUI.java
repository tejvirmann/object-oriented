  /**
   * This interface is used by the Button class and the Animal class. Each time 
   * there is a call to this interface, then a method in this interface is run. 
   * However, due to polymorphism, and overriding, when a method is called, the function
   * of the method may be different. 
   * 
   * For example. If a type tiger is calling mousePressed, if what is calling it 
   * is of type animal, or button, then a different method/outcome will be run.
   * 
   */

public interface ParkGUI {
  public void draw(); // draws a ParkGUI object (either an animal or a button) to the display window

  public void mousePressed(); // called each time the mouse is Pressed

  public void mouseReleased(); // called each time the mouse is Pressed

  public boolean isMouseOver(); // checks whether the mouse is over a ParkGUI object

}